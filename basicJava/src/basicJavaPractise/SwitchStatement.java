package basicJavaPractise;

public class SwitchStatement {

	public static void main(String[] args) {
		
		
		int score = 50;
		
		switch(score)
		{
		case 45:
			System.out.println("The score is very good");
			break;
		case 40:
			System.out.println("The score is good");
			break;
		case 35:
			System.out.println("The score is ok");
			break;
		case 30:
			System.out.println("The score is ok");
			break;
		default:
			System.out.println("The score is not defined");
			break;
			
			
		}
		

	}

}
